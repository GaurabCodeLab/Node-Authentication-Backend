const mongoose = require("mongoose");

const { Schema, models, model } = mongoose;

const userSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
});

exports.User = models.User || model("User", userSchema);